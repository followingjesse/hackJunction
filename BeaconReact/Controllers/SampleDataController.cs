using System;
using System.Collections.Generic;
using System.Linq;
using BeaconBackend;
using Microsoft.AspNetCore.Mvc;
using Newtonsoft.Json;

namespace BeaconReact.Controllers
{
    [Route("api/[controller]")]
    public class SampleDataController : Controller
    {
        private static string[] Summaries = new[]
        {
            "Freezing", "Bracing", "Chilly", "Cool", "Mild", "Warm", "Balmy", "Hot", "Sweltering", "Scorching"
        };

        [HttpGet("[action]")]
        public IEnumerable<WeatherForecast> WeatherForecasts()
        {
            var rng = new Random();
            return Enumerable.Range(1, 5).Select(index => new WeatherForecast
            {
                DateFormatted = DateTime.Now.AddDays(index).ToString("d"),
                TemperatureC = rng.Next(-20, 55),
                Summary = Summaries[rng.Next(Summaries.Length)]
            });
        }

        [HttpGet("[action]")]
        public IEnumerable<Result> Articles()
        {
            var response = new SearchService().MakeRequest();
            string responseJson = response.Result.Content.ReadAsStringAsync().Result;
            Articles articles = JsonConvert.DeserializeObject<Articles>(responseJson);

            articles.results.ForEach(r => Console.WriteLine(r.title));
            return articles.results;
        }

        public class WeatherForecast
        {
            public string DateFormatted { get; set; }
            public int TemperatureC { get; set; }
            public string Summary { get; set; }

            public int TemperatureF
            {
                get
                {
                    return 32 + (int)(TemperatureC / 0.5556);
                }
            }
        }
    }
}
